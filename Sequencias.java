
public class Sequencias {

	public static void main(String[] args) {
		// a) 1, 3, 5, 7, ___
        int a = 7 + 2;
        System.out.println("Próximo elemento da sequência a: " + a);

        // b) 2, 4, 8, 16, 32, 64, ____
        int b = 64 * 2;
        System.out.println("Próximo elemento da sequência b: " + b);

        // c) 0, 1, 4, 9, 16, 25, 36, ____
        int c = 7 * 7;
        System.out.println("Próximo elemento da sequência c: " + c);
        
        // d) 4, 16, 36, 64, ____
        int d = 10 * 10;
        System.out.println("Próximo elemento da sequência d: " + d);

        // e) 1, 1, 2, 3, 5, 8, ____
        int e1 = 5;
        int e2 = 8;
        int e = e1 + e2;
        System.out.println("Próximo elemento da sequência e: " + e);

        // f) 2, 10, 12, 16, 17, 18, 19, ____
        int f = 19 + 1;
        System.out.println("Próximo elemento da sequência f: " + f);

	}

}
